export function mergeWithDefaults(saved) {
  const base = defaultContent();
  if (!saved || typeof saved !== "object") return base;
  return {
    ...base,
    ...saved,
    hero: { ...base.hero, ...(saved.hero || {}) },
    about: { ...base.about, ...(saved.about || {}) },
    cta: { ...base.cta, ...(saved.cta || {}) },
    contact: { ...base.contact, ...(saved.contact || {}) },
    seo: { ...base.seo, ...(saved.seo || {}) },
    images: { ...base.images, ...(saved.images || {}) },
    services:
      Array.isArray(saved.services) && saved.services.length
        ? saved.services
        : base.services,
    advantages:
      Array.isArray(saved.advantages) && saved.advantages.length
        ? saved.advantages
        : base.advantages,
    stats:
      Array.isArray(saved.stats) && saved.stats.length
        ? saved.stats
        : base.stats
  };
}
